use super::*;
use std::collections::BTreeMap;
use tempfile::TempDir;

fn make_lock_package(name: &str, bins: &[&str]) -> LockPackage {
    LockPackage {
        name: name.to_string(),
        description: None,
        version: "1.0.0".to_string(),
        bin: bins.iter().map(|s| (*s).to_string()).collect(),
        require: BTreeMap::new(),
        require_dev: BTreeMap::new(),
        replace: BTreeMap::new(),
        provide: BTreeMap::new(),
        conflict: BTreeMap::new(),
        autoload: composer_manifest::lockfile::LockAutoload::default(),
        autoload_dev: serde_json::Value::Null,
        extra: serde_json::Value::Null,
        version_normalized: None,
        dist: None,
        source: None,
        transport_options: serde_json::Value::Null,
        package_type: None,
        time: None,
        license: Vec::new(),
        funding: Vec::new(),
    }
}

fn setup_package_bin(root: &Path, pkg_name: &str, bin_path: &str, content: &[u8]) {
    let full = root.join("vendor").join(pkg_name).join(bin_path);
    std::fs::create_dir_all(full.parent().unwrap()).unwrap();
    std::fs::write(&full, content).unwrap();
}

#[test]
fn php_bin_generates_php_proxy() {
    let tmp = TempDir::new().unwrap();
    let root = tmp.path();
    setup_package_bin(root, "acme/tool", "bin/run", b"<?php\necho 'hello';\n");

    let pkg = make_lock_package("acme/tool", &["bin/run"]);
    let pkgs: Vec<&LockPackage> = vec![&pkg];
    let summary = install_bin_proxies(root, &pkgs);

    assert_eq!(summary.bins_installed, 1);
    assert!(summary.warnings.is_empty(), "{:?}", summary.warnings);

    let proxy = root.join("vendor/bin/run");
    assert!(proxy.exists());
    let content = std::fs::read_to_string(&proxy).unwrap();
    assert!(content.starts_with("#!/usr/bin/env php\n<?php"));
    assert!(content.contains("Proxy PHP file generated by Composer"));
    assert!(content.contains("$GLOBALS['_composer_bin_dir']"));
    assert!(content.contains("$GLOBALS['_composer_autoload_path']"));
    assert!(content.contains("'/..'.'/acme/tool/bin/run'"));
}

