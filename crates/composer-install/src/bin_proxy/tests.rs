#[test]
fn multiple_packages_multiple_bins() {
    let tmp = TempDir::new().unwrap();
    let root = tmp.path();
    setup_package_bin(root, "acme/foo", "bin/foo", b"<?php\necho 1;\n");
    setup_package_bin(root, "acme/bar", "bin/bar", b"#!/bin/sh\necho hi\n");
    setup_package_bin(root, "acme/bar", "bin/baz", b"<?php\necho 2;\n");

    let pkg1 = make_lock_package("acme/foo", &["bin/foo"]);
    let pkg2 = make_lock_package("acme/bar", &["bin/bar", "bin/baz"]);
    let pkgs: Vec<&LockPackage> = vec![&pkg1, &pkg2];
    let summary = install_bin_proxies(root, &pkgs);

    assert_eq!(summary.bins_installed, 3);
    assert!(root.join("vendor/bin/foo").exists());
    assert!(root.join("vendor/bin/bar").exists());
    assert!(root.join("vendor/bin/baz").exists());

    let foo_content = std::fs::read_to_string(root.join("vendor/bin/foo")).unwrap();
    assert!(foo_content.contains("Proxy PHP file generated by Composer"));

    let bar_content = std::fs::read_to_string(root.join("vendor/bin/bar")).unwrap();
    assert!(bar_content.contains("COMPOSER_RUNTIME_BIN_DIR"));
}
