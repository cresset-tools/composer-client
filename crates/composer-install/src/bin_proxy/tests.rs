#[cfg(unix)]
#[test]
fn proxy_is_executable() {
    use std::os::unix::fs::PermissionsExt;

    let tmp = TempDir::new().unwrap();
    let root = tmp.path();
    setup_package_bin(root, "acme/tool", "bin/run", b"<?php\necho 1;\n");

    let pkg = make_lock_package("acme/tool", &["bin/run"]);
    let pkgs: Vec<&LockPackage> = vec![&pkg];
    install_bin_proxies(root, &pkgs);

    let proxy_mode = root
        .join("vendor/bin/run")
        .metadata()
        .unwrap()
        .permissions()
        .mode();
    assert_ne!(proxy_mode & 0o111, 0, "proxy must be executable");

    let target_mode = root
        .join("vendor/acme/tool/bin/run")
        .metadata()
        .unwrap()
        .permissions()
        .mode();
    assert_ne!(target_mode & 0o111, 0, "target must be executable");
}

