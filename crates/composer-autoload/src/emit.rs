pub(crate) fn files(entries: &[FileEntry]) -> String {
    let mut out =
        String::with_capacity(FILES_HEADER.len() + entries.len() * 80 + ARRAY_FOOTER.len());
    out.push_str(FILES_HEADER);
    for e in entries {
        out.push_str("    '");
        out.push_str(&e.identifier);
        out.push_str("' => ");
        out.push_str(&e.path_expr);
        out.push_str(",\n");
    }
    out.push_str(ARRAY_FOOTER);
    out
}

