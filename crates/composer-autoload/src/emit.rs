//! PHP-source emitters for the autoload files.
//!
//! Every file Composer emits shares a fixed prolog and epilog. The
//! per-file contents between them are a single `return array(...)`
//! (or for `autoload.php`, the entry-point bootstrap). Output is
//! byte-equivalent to Composer 2.8 — golden fixtures in
//! `tests/fixtures/*/expected/` are the contract.

pub(crate) mod platform_check;
pub(crate) mod real;
pub(crate) mod static_loader;

use crate::collect::{ClassmapEntry, Entry, FileEntry};

const PSR4_HEADER: &str = "<?php\n\n// autoload_psr4.php @generated by Composer\n\n$vendorDir = dirname(__DIR__);\n$baseDir = dirname($vendorDir);\n\nreturn array(\n";
const PSR0_HEADER: &str = "<?php\n\n// autoload_namespaces.php @generated by Composer\n\n$vendorDir = dirname(__DIR__);\n$baseDir = dirname($vendorDir);\n\nreturn array(\n";
const FILES_HEADER: &str = "<?php\n\n// autoload_files.php @generated by Composer\n\n$vendorDir = dirname(__DIR__);\n$baseDir = dirname($vendorDir);\n\nreturn array(\n";
const CLASSMAP_HEADER: &str = "<?php\n\n// autoload_classmap.php @generated by Composer\n\n$vendorDir = dirname(__DIR__);\n$baseDir = dirname($vendorDir);\n\nreturn array(\n";
const ARRAY_FOOTER: &str = ");\n";

pub(crate) fn psr4(entries: &[Entry]) -> String {
    let mut out =
        String::with_capacity(PSR4_HEADER.len() + entries.len() * 64 + ARRAY_FOOTER.len());
    out.push_str(PSR4_HEADER);
    for e in entries {
        out.push_str("    ");
        out.push_str(&php_single_quoted(&e.prefix));
        out.push_str(" => array(");
        for (i, p) in e.paths.iter().enumerate() {
            if i > 0 {
                out.push_str(", ");
            }
            out.push_str(p);
        }
        out.push_str("),\n");
    }
    out.push_str(ARRAY_FOOTER);
    out
}

